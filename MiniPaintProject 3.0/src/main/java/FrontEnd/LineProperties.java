    private void addLineButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addLineButtonActionPerformed
        if (startingXCoordinateTextField.getText().equals("") || endingXCoordinateTextField.getText().equals("") || startingYCoordinateTextField.getText().equals("") || endingYCoordinateTextField.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Some fields are empty");
        } else {
            if (isIntger(startingXCoordinateTextField.getText()) && isIntger(endingXCoordinateTextField.getText()) && isIntger(startingYCoordinateTextField.getText()) && isIntger(endingYCoordinateTextField.getText())) {
                //int lineCounter = 0;
                int startingXCor = Integer.parseInt(startingXCoordinateTextField.getText());
                int endingXCor = Integer.parseInt(endingXCoordinateTextField.getText());
                int startingYCor = Integer.parseInt(startingYCoordinateTextField.getText());
                int endingYCor = Integer.parseInt(endingYCoordinateTextField.getText());
                LineSegment line = new LineSegment(new Point(startingXCor, startingYCor), new Point(endingXCor, endingYCor));
                line.setColor(this.borderColor);
                DrawingFrame.getPanel().addShape(line);
                DrawingFrame.getPanel().refresh(null);
                DrawingFrame.getCombobox().addItem("Line" + DrawingFrame.getPanel().getShapes().length);

                this.setVisible(false);
                ((JFrame) getParentNode()).setVisible(true);
            }
            else
              JOptionPane.showMessageDialog(null, "Please enter numeric values in all fields");  
        }

    }//GEN-LAST:event_addLineButtonActionPerformed

